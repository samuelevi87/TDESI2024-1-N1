package Exercicio01;

public class CorrecaoPessoa {
    private String nome;
    private int idade;

    public CorrecaoPessoa(String nome, int idade) {
        this.nome = nome;
        this.idade = idade;
    }

    public String getNome() {
        return nome;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getIdade() {
        return idade;
    }
}
